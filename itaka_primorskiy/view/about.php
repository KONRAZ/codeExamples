                          Среди 27 тысяч гектаров, на которых расположен Курортный район, только 250 га занято жилой, коммерческой и административной застройкой. <br>
                          Район имеет богатую историю: уже во времена российской империи здесь стали строить свои загородные усадьбы дворяне и богатые купцы, представители богемы. После октябрьской революции в Курортном районе с удовольствием селились партийные деятели и советские чиновники.
