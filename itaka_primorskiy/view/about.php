                        <h3>Кронштадт</h3>
                        <div class="description">
                          В непосредственной близости к Приморскому району в окружении Финского залива  находится город-порт Кронштадт. Он расположен на острове Котлин, и, будучи крепостью, оказывался на передовой в многочисленных морских сражениях, защищая подступы к Северной Столице. На сегодня это один из самых своеобразных районов Санкт-Петербурга. На небольшой площади — всего 19 с небольшим квадратных километров — расположены исторические достопримечательности (недавно отреставрированный Морской собор, крепостные форты, музей истории Кронштадта, дерево желаний), судостроительные заводы, военные части морского флота России. <br>

                          <br>
                            <div class="collapse" id="viewdetails4">
                              Кронштадт поражает приезжих не только памятниками истории, но и вычерченными линиями улиц, множеством садов и скверов, чудесными пейзажами, хорошей экологией и чистыми пляжами. Кронштадт красив в любое время года. Его парки и скверы наполнены необыкновенным очарованием. Крики чаек, морские бризы, солнечные зайчики на воде залива, гранитные набережные придают городу своеобразие и неповторимость.<br>
                                  Значительную часть жилищного фонда Кронштадта занимают дома постройки до 1917 г. Массовое жилищное строительство развернулось в 1930-х (треть жилфонда занимают сталинки), а затем продолжилось в хрущевские времена. В 80-х годах прошлого века, при въезде в город, был возведен микрорайон Цитадель (квартал 19) с современными 7-12-этажными домами.<br>
